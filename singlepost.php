        <h1>Spacecraft Could Nuke Dangerous Asteroid to Defend Earth</h1>
        <span>MARCH 9,2018</span>
        <p>The next time a hazardous asteroid lines Earth up in its crosshairs, we may be ready for the threat.
          Scientists and engineers with the U.S. government have drawn up plans for a spacecraft that could knock big, incoming space rocks off course via blunt-force 
          impact or blow them to bits with a nuclear warhead, BuzzFeed News reported.</p>
        <p>Each HAMMER spacecraft would weigh about 8.8 tons (8 metric tons). If an asteroid threat is detected early enough, a fleet of the vehicles could be dispatched to 
          collide, nuke-free, with the space rock, changing its trajectory enough to spare Earth from an impact.</p>
        <p>But this strategy wouldn't work for big asteroids that appear out of the cosmic gloom with little warning; there wouldn't be enough time for the nudge to take 
          effect. To neutralize, or at least mitigate, such threats, a HAMMER would have to be outfitted with a nuke, the study team stressed.</p>
        <p>"Whenever practical, the kinetic impactor is the preferred approach, but various factors, such as large uncertainties or short available response time, reduce the 
          kinetic impactor's suitability and, ultimately, eliminate its sufficiency," the researchers wrote in the Acta Astronautica paper. The research is part of a broader 
          study by NASA and the National Nuclear Security Agency to better understand humanity's options when presented with a potentially threatening near-Earth object (NEO).</p>
        <p>To arrive at these results, and refine HAMMER's design, the team modeled how to deal with a potential real-life scenario: What if the 1,640-foot-wide (500 meters) 
          near-Earth asteroid Bennu was headed right for our planet? There's a 1-in-2,700 chance that this will indeed happen, on Sept. 21, 2135, BuzzFeed News reported.</p>
        <p>"Bennu was selected for our case study in part because it is the best-studied of the known NEOs," the researchers wrote. "It is also the destination of NASA's OSIRIS-REx sample-return mission, which is, at the time of this writing, en route to Bennu following a September 2016 launch."
          
          OSIRIS-REx — whose name is short for Origins, Spectral Interpretation, Resource Identification, Security, Regolith Explorer — is scheduled to arrive at Bennu this summer. The spacecraft will study the asteroid from orbit for two years, then swoop in to grab a sample, which will return to Earth in a special container in September 2023, if all goes according to plan.
          
          OSIRIS-REx is an $800 million mission. It's unclear how much a HAMMER spacecraft would cost, if one ever gets built (which is certainly no guarantee, given that it's a concept vehicle at this stage). The study team declined to estimate a price, BuzzFeed News reported.</p>
