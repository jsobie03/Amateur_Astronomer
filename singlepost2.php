        <h1>ULTRAVIOLET RADIATION FROM LOW-MASS STARS COULD RENDER PLANETS UNINHABITABLE</h1>
        <span>MAy 18,2018</span>
        <p> Low-mass stars are currently the most promising targets when searching for potentially habitable planets, but new research has revealed that some of these stars produce significant amounts of ultraviolet (UV) radiation throughout their lifetimes. Such radiation could hinder the development of life on any orbiting planets.</p> 

<p>M-dwarfs are stars that are cooler and less massive than stars like our Sun, and are the most common type of star in the Galaxy, meaning that it is vital that we better understand them and the influence they have on their planets.</p>

<p>Detecting terrestrial planets in the habitable zone — the region where liquid water can exist on a planet's surface — when they pass in front of, or transit, Sun-like stars is difficult. This is partly because we only see a small dip in the light as the planet crosses the star, and also partly because their orbits are long enough that we have to wait several years to observe multiple transits. However, because M-dwarfs are smaller and cooler, the planets in their habitable zone are much closer to their star, resulting in larger and more frequent drops in light, making them easier to detect.</p>

<p>This makes M-dwarfs ideal candidates when searching for potentially habitable planets, which has led to habitable zone terrestrial planets being discovered around M-dwarfs including Proxima Centauri, TRAPPIST-1 and Ross 128.</p>

<h1>Ultraviolet levels over time</h1>

<p>A paper by astrophysicists Adam Schneider and Evgenya Shkolnik from Arizona State University, recently published in The Astronomical Journal, has revealed that the hottest and most massive M-dwarfs, referred to as 'early type', emit different amounts of UV radiation over their lifetime compared to the less massive and cooler 'mid-' and 'late-type' M-dwarfs. The paper used observations from NASA's Galaxy Evolution Explorer (GALEX) spacecraft to study several populations of M-dwarfs in ultraviolet light.</p>

<p>M-dwarfs are known to emit higher levels of potentially harmful UV radiation than stars like our Sun. UV radiation can erode planetary atmospheres and have a detrimental effect on biology. It can also affect the abundances of molecules in planetary atmospheres, including carbon dioxide, oxygen and ozone. Ultraviolet light can break down molecules of carbon dioxide into their atomic components, producing atomic oxygen that then combines with molecular oxygen to form ozone. Ozone is easier to detect than oxygen, and is often thought to be a potential biomarker for life, so excessive UV radiation resulting in extra ozone could produce false positives where we mistake the extra ozone as a biological product. Therefore, understanding the levels of UV radiation emitted by M-dwarfs is essential for assessing the observations of their atmospheres.</p>

<p>The aim of the 'Habitable Zones and M-dwarf Activity across Time" (HAZMAT) program is to use UV observations to understand how the habitability of low-mass stars changes over time. The researchers used GALEX to observe a large sample of M-dwarfs with known ages between ten million years and five billion years old. </p>

<p>Their results revealed that the lower-mass mid- and late-type stars retain high levels of UV activity with only a very gradual decrease over time, compared to the early-type M-dwarfs where the levels of UV radiation drop off quicker as the stars age.</p> 

<p>The levels of UV radiation are very different across the early-type M-dwarfs, but this is not seen in the late-type stars. This could be due to the influence of stellar rotation. The lowest mass stars are fully convective, which means that the stellar material rises and falls in convective currents throughout the whole star. Higher mass stars are split into different zones, with a radiative zone as well as a convective zone where the energy spreads through radiation. Stars begin their lives rotating rapidly and then spin down over time as they lose momentum through the stellar wind. The stellar wind does not function efficiently in fully convective stars, so it is expected that the late-type stars would stay rotating rapidly for much longer than the early-type stars. Rotation is directly related to activity, so this could explain why lower mass stars are active for longer.</p>

